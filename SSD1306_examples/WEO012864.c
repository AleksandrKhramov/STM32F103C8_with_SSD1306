//-------------------------------------------------
//          Write Data Function
//-------------------------------------------------
void Write_data(UB8 datap)
{
         LCM_RS = 1;
         LCM_RW = 0;
         LCM_CS1 = 0; 
         LCM_PORT=datap;
	 LCM_ENABLE = 1;
	 _nop_();
	 LCM_ENABLE = 0;
}
//-------------------------------------------------
//     WRITE_COMMAND
//-------------------------------------------------
void Write_command(UB8 command)
{
         LCM_RS = 0;
         LCM_RW = 0; 
         LCM_CS1 = 0;
         LCM_PORT = command;
	 LCM_ENABLE = 1;
	 _nop_();
	 LCM_ENABLE = 0;
}

