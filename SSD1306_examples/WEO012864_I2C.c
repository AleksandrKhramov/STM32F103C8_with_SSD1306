void WriteBit(unsigned char onebit)
{
    unsigned char i;

	for(i=0;i<=7;i++)
    {
    	SCL=0;
    	SDA=(onebit<<i)&0x80;
    	SCL=1;
    	SCL=0;

    }
 	SCL=0;
  	SDA=1;
    SCL=1;
	SCL=0;   	    		    
}
void Write_Cmd(unsigned char cmd)
{	
	SDA=1;
	SCL=1;
	SDA=0;

	WriteBit(0x78);
	WriteBit(0x00);
 	WriteBit(cmd);
	
	SDA=1;
	SCL=1;
	SDA=0;
}

void Write_Data(unsigned char dat)
{
 	SDA=1;
	SCL=1;
	SDA=0;

	WriteBit(0x78);
	WriteBit(0x40);
 	WriteBit(dat);

	SDA=0;
	SCL=1;
	SDA=1;
}


