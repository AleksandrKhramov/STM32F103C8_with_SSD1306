void Write_Cmd(unsigned char cmd)
{
    unsigned char bMask;

	LCM_CS = 0;
	LCM_CD = 0;
	for (bMask = 0x80; bMask; bMask >>= 1)
           {
            DIO = cmd & bMask;
            SCL = 0;	
	        SCL = 1;
	   }           
	LCM_CS = 1;
	_nop_();
}

void Write_Data(unsigned char dat)
{
	unsigned char bMask;

	LCM_CS = 0;
	LCM_CD = 1;
	for (bMask = 0x80; bMask; bMask >>= 1)
           {
            DIO = dat & bMask;
            SCL = 0;	
	        SCL = 1;
	   }           
	LCM_CS = 1;
	_nop_();
}


